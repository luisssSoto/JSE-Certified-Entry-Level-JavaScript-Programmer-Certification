"use strict";
//Decrement
//prefix
let x = 0;
console.log(x);
console.log(--x);
console.log(x);

//post-fix
let y = 0;
console.log(y);
console.log(y--);
console.log(y);
console.log();

//Increment
//pre-fix
let z = 0;
console.log(z);
console.log(++z);
console.log(z);

//post-fix
let a = 0;
console.log(a);
console.log(a++);
console.log(a);
console.log();

//Some operations under 252 integer are imprecise:
console.log(0.2 + 0.1); // 0.30000000000000004
console.log(0.2 * 0.1); // 0.020000000000000004
console.log(0.3 / 0.1); // 2.999999999999999

