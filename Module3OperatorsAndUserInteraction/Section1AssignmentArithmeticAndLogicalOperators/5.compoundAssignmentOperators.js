"use strict";
//Compound assignment operators

//Binary operators + assignment operators = ...
//Addition assignment

let x = 10;
x += 2;
console.log(x);

//Substraction assignment
x -= 4;
console.log(x);

//Multiplication assignment
x *= 2;
console.log(x);

//Division assignment
x /= 4;
console.log(x);

//Power assignment
x **= 3;
console.log(x);

//Module assignmente
x %= 20;
console.log(x);